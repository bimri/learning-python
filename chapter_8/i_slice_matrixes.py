'''Because lists are sequences, indexing and slicing work the same way for lists as they do
for strings'''
L = ['nyathi', 'oluchi', 'BIMRI!']
print(L[2])
print(L[-2])
print(L[1:])


# Nesting lists => Matrixes
matrix = [[1, 2, 3],[4, 5, 6], [7, 8, 9]]
print(matrix[1])
print(matrix[1][1])
print(matrix[2][0])
